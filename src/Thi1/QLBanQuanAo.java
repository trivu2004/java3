    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        String masp = txtMaSP.getText();
        String loai = (String) cboLoai.getSelectedItem();
        String gia = txtGia.getText();
        String soluong = txtSoLuong.getText();
        String thanhtien = "" + Integer.parseInt(gia) * Integer.parseInt(soluong);
        try {
            pstInsert.setString(1, masp);
            pstInsert.setString(2, loai);
            pstInsert.setString(3, soluong);
            pstInsert.setString(4, thanhtien);
            int addRows = pstInsert.executeUpdate();
            loadData();
            reset();
            if (addRows > 0) {
                JOptionPane.showMessageDialog(this, "Lưu Sản phẩm thành công!", "Successfull", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Failed to save data in database", "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnLuuActionPerformed

    private void cboLoaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboLoaiActionPerformed
        try {
            String sql2 = "select Loai,Gia from LoaiSP where Loai=?";
            PreparedStatement pstLoai;

            pstLoai = con.prepareStatement(sql2);
            pstLoai.setString(1, (String) cboLoai.getSelectedItem());
            ResultSet rts = pstLoai.executeQuery();
            while (rts.next()) {
                txtGia.setText(rts.getString(2));
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_cboLoaiActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        try {
            int i = tblThongTin.getSelectedRow();
            if (i >= 0) {
                SanPham sp = list.get(i);
                if (sp.getMaSP().equals(txtMaSP.getText())) {
                    pstUpdate.setString(4, txtMaSP.getText().trim());
                    pstUpdate.setString(1, (String) cboLoai.getSelectedItem());
                    pstUpdate.setString(2, txtSoLuong.getText().trim());
                    String thanhtien = "" + Integer.parseInt(txtGia.getText()) * Integer.parseInt(txtSoLuong.getText());
                    pstUpdate.setString(3, thanhtien);
                    pstUpdate.executeUpdate();
                    loadData();
                    JOptionPane.showMessageDialog(this, "Cập nhật thành công!");
                    reset();
                } else {
                    pstInsert.setString(1, txtMaSP.getText().trim());
                    pstInsert.setString(2, (String) cboLoai.getSelectedItem());
                    pstInsert.setString(3, txtSoLuong.getText().trim());
                    pstInsert.setString(4, txtGia.getText().trim());
                    String thanhtien = "" + Integer.parseInt(txtGia.getText()) * Integer.parseInt(txtSoLuong.getText());
                    pstInsert.setString(5, thanhtien);
                    pstInsert.executeUpdate();
                    this.loadData();
                    JOptionPane.showMessageDialog(this, "Đã thêm Sản phẩm thành công!");
                    reset();
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_btnSuaActionPerformed

