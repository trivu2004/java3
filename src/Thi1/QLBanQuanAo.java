    public void Table() {
        tableModel = new DefaultTableModel();
        String[] columns = {"Mã SP", "Loại", "Giá", "Số Lượng", "Thành Tiền"};
        tableModel.setColumnIdentifiers(columns);
        tblThongTin.setModel(tableModel);
    }

    public void filltable() {
        tableModel.setRowCount(0);
        for (SanPham sp : list) {
            Object[] row = {sp.getMaSP(), sp.getLoai(), sp.getGia(), sp.getSoLuong(), sp.getThanhTien()};
            tableModel.addRow(row);
        }
    }

    public void Display(int i) {
        SanPham sp = list.get(i);
        txtMaSP.setText(sp.getMaSP());
        cboLoai.setSelectedItem(sp.getLoai());
        txtGia.setText(sp.getGia());
        txtSoLuong.setText(sp.getSoLuong());
        lblThanhTien.setText(sp.getThanhTien());
    }

    public void loadData() {
        String sql = "Select MaSP,SanPham.Loai,LoaiSP.Gia,SoLuong,ThanhTien from SanPham join LoaiSP on SanPham.Loai = LoaiSP.Loai";
        try {
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            list.clear();
            while (rs.next()) {
                String masp = rs.getString(1);
                String loai = rs.getString(2);
                String gia = rs.getString(3);
                String soluong = rs.getString(4);
                String thanhtien = rs.getString(5);
                SanPham hh = new SanPham(masp, loai, gia, soluong, thanhtien);
                list.add(hh);
                filltable();
            }
            rs.close();
            st.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }

    public void reset() {
        txtMaSP.setText("");
        txtSoLuong.setText("");
        lblThanhTien.setText("0");
    }

